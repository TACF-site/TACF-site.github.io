        function hmsToSecondsOnly(str) {
        var p = str.split(':'),
            s = 0, m = 1;

        while (p.length > 0) {
            s += m * parseInt(p.pop(), 10);
            m *= 60;
        }

        return s;
    }

    function encontrarIndiceTempoMaisProximo(tempoInserido, arrayDeTempos) {
        segundosInserido = tempoInserido;
        
        let indiceMaisProximo = 0;
        let menorDiferenca = Infinity;
        
        for (let i = 0; i < arrayDeTempos.length; i++) {
            const segundosTabela = hmsToSecondsOnly(arrayDeTempos[i]);
            const diferenca = Math.abs(segundosTabela - segundosInserido);
            
            if (diferenca < menorDiferenca) {
                menorDiferenca = diferenca;
                indiceMaisProximo = i;
            }
        }
        
        return indiceMaisProximo;
    }


